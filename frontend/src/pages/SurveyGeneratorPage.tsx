// ─── helpers ────────────────────────────────────────────────────────────────

function esc(s: string) {
  return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

function buildFieldHtml(field: SurveyField, i: number): string {
  const req = field.required ? '<span style="color:#ef4444">*</span>' : '';
  const nm = `f_${i}`;
  const lbl = `<div class="field"><label for="${nm}">${esc(field.label)} ${req}</label>`;
  let inp = '';
  switch (field.type) {
    case 'textarea':
      inp = `<textarea id="${nm}" name="${nm}" placeholder="${esc(field.placeholder ?? '')}"${field.required ? ' required' : ''}></textarea>`;
      break;
    case 'select':
      inp = `<select id="${nm}" name="${nm}"${field.required ? ' required' : ''}><option value="">Select…</option>${(field.options ?? []).map((o) => `<option value="${esc(o)}">${esc(o)}</option>`).join('')}</select>`;
      break;
    case 'radio':
      inp = `<div class="opts">${(field.options ?? []).map((o) => `<label><input type="radio" name="${nm}" value="${esc(o)}"${field.required ? ' required' : ''}/> ${esc(o)}</label>`).join('')}</div>`;
      break;
    case 'checkbox':
      inp = `<div class="opts">${(field.options ?? []).map((o) => `<label><input type="checkbox" name="${nm}[]" value="${esc(o)}"/> ${esc(o)}</label>`).join('')}</div>`;
      break;
    case 'rating':
      inp = `<div class="stars">${Array.from({ length: field.max ?? 5 }, (_, k) => `<input type="radio" name="${nm}" id="${nm}_${k + 1}" value="${k + 1}"/><label for="${nm}_${k + 1}">★</label>`).join('')}</div>`;
      break;
    default:
      inp = `<input type="${esc(field.type)}" id="${nm}" name="${nm}" placeholder="${esc(field.placeholder ?? '')}"${field.required ? ' required' : ''}/>`;
  }
  return `${lbl}${inp}</div>`;
}

function generateHtml(survey: GeneratedSurvey): string {
  const fields = survey.fields.map(buildFieldHtml).join('\n');
  return `<!DOCTYPE html>
<html lang="en"><head>
<meta charset="UTF-8"/>
<meta name="viewport" content="width=device-width,initial-scale=1.0"/>
<title>${esc(survey.title)}</title>
<style>
*,*::before,*::after{box-sizing:border-box}
body{font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',sans-serif;max-width:680px;margin:40px auto;padding:0 20px;color:#1e293b;background:#fff;line-height:1.6}
h1{font-size:1.5rem;font-weight:700;margin-bottom:6px}
p.d{color:#64748b;margin-bottom:32px;font-size:.95rem}
.field{margin-bottom:20px}
label{display:block;font-size:.875rem;font-weight:600;color:#374151;margin-bottom:6px}
input,textarea,select{width:100%;padding:8px 12px;border:1px solid #d1d5db;border-radius:6px;font-size:.9rem;color:#1e293b;background:#f9fafb}
input:focus,textarea:focus,select:focus{outline:none;border-color:#2563eb;background:#fff}
textarea{resize:vertical;min-height:80px}
.opts label{font-weight:400;display:flex;align-items:center;gap:8px;margin-bottom:4px}
.stars input[type=radio]{display:none}.stars label{font-size:1.5rem;cursor:pointer;color:#d1d5db;margin:0}
.btn{display:block;width:100%;padding:12px;background:#2563eb;color:#fff;border:none;border-radius:8px;font-size:1rem;font-weight:600;cursor:pointer;margin-top:32px}
.btn:hover{background:#1d4ed8}
footer{margin-top:48px;text-align:center;font-size:.75rem;color:#94a3b8}
@media print{body{margin:0}.btn{display:none}}
</style></head>
<body>
<h1>${esc(survey.title)}</h1>
${survey.description ? `<p class="d">${esc(survey.description)}</p>` : ''}
<form>
${fields}
<button type="submit" class="btn">Submit</button>
</form>
<footer>Generated by ISET Observatory · Survey Generator</footer>
</body></html>`;
}

